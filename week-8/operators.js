// Logical Operators
let g = true;
let h = false;

// two siblings of the same surname
// John Doe - Jane Doe

if (g && h) { // and
    console.log('Yes, both are true');
}

// John Doe - Jane Smith
if (g || h) { // or
    console.log('Yes, one of them is true');
}

